    Map::Map() {
        cout << "Map created ..." << endl;
        dirt = TextureManager::loadTexture("../assets/dirt.png");
        grass = TextureManager::loadTexture("../assets/grass.png");
        water = TextureManager::loadTexture("../assets/water.png");

        loadMap(level1);

        startRect.x = startRect.y = endRect.x = endRect.y = 0;
        startRect.w = endRect.w = 32;
        startRect.h = endRect.h = 32;

    }

    void Map::loadMap(int arr[20][25]) {
        for(int row = 0; row < 20; row ++) {
            for(int col = 0; col < 25; col++) {
               map[row][col] = arr[row][col]; 
            }
        }
    }

    void Map::drawMap() {
        int type = 0;
        for(int row = 0; row < 20; row ++) {
            for(int col = 0; col < 25; col++) {
                type = map[row][col];
                endRect.x = col * 32;
                endRect.y = row * 32;
                switch(type) {
                    case 0:
                        TextureManager::draw(water, startRect, endRect);
                        break;
                    case 1:
                        TextureManager::draw(grass, startRect, endRect);
                        break;
                    case 2:
                        TextureManager::draw(dirt, startRect, endRect);
                    default:
                        break;
                }   
            }
        }    
    }
